package com.winiardev.nails.account.embeddable;

/**
 * Enumeration representing possible roles in the system.
 * <p>
 * The available roles are:
 * <ul>
 * <li>ADMIN: Represents an administrator with elevated privileges.</li>
 * <li>CUSTOMER: Represents a standard user or customer with basic access.</li>
 * </ul>
 * 
 * @author winiar.dev
 */
public enum Role {
    ADMIN,
    CUSTOMER
}